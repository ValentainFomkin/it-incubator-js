/*Функция голов Месси
Месси - футболист с голами в трех лигах:

Ла Лига
Копа дель Рей
Лига чемпионов
Выполните функцию, чтобы вернуть общее количество его голов во всех трех лигах.

Примечание: входные данные всегда будут действительными.

Например:

5, 10, 2 --> 17*/

function goals(laLigaGoals, copaDelReyGoals, championsLeagueGoals) {
  return laLigaGoals + copaDelReyGoals + championsLeagueGoals;
}
